import skinService from "../service/skinService"
const readSkins = async (req, res) => {
    try {
        let data = await skinService.getSkins();
        return res.status(200).json({
            EM: data.EM,
            EC: data.EC,
            DT: data.DT
        })
    } catch (e) {
        console.log(e)
        return res.status(500).json({
            EM: "Error from server",
            EC: "-1",
            DT: ""
        })
    }
}

